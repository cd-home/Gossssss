package main

func main() {
	//  func
	// 	函数名
	//	参数列表, 返回值列表 (作为局部变量)

	// 返回值可以是单独一个, 有名、无名(无名可 不加括号)
	// 支持多个返回值
	// 如果, 返回值是有名字的, return 可以省略需要返回的变量 (一般不建议这样使用)

	// 实参
	// 形参

	// 函数的类型被称为函数的签名
	// 如果两个函数形式参数列表和返回值列表中的变量类型一一对应，那么这两个函数被认为有相同的类型或签名
	// 变量的名字不影响函数的签名
}

